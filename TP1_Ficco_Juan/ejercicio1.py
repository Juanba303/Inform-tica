# Se pide la cantidad de kilómetros y se pasa a millas
km = float(input("Cantidad de kilómetros: "))
print(f"{km} km equivalen a {km / 1.60934:.2f} millas")

# Se pide la cantidad de millas y se pasa a kilómetros
millas = float(input("Cantidad de millas: "))
print(f"{millas} millas equivalen a {millas * 1.60934:.2f} km")